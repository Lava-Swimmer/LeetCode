from typing import List


class BinaryMatrix:
    minColumn = float('inf')

    def __init__(self, matrix: List[List[int]]):
        self.matrix = matrix
        self.count = 0

    def get(self, x: int, y: int) -> int:
        if self.count > 1000:
            raise RuntimeError('too many get')
        self.count += 1
        return self.matrix[x][y]

    def dimensions(self) -> List[int]:
        return [len(self.matrix), len(self.matrix[0])]


class Solution:
    def leftMostColumnWithOne(self, binaryMatrix: BinaryMatrix) -> int:
        minColumn = float('inf')
        total_row, total_column = binaryMatrix.dimensions()
        for i in range(total_row):
            indexOfLeftMostColumnWithOne = self.getIndexOfLeftMostColumnWithOne(binaryMatrix, i, 0,
                                                                                min(total_column - 1,
                                                                                    minColumn - 1))
            if indexOfLeftMostColumnWithOne == 0:
                return 0

            if 0 < indexOfLeftMostColumnWithOne < minColumn:
                minColumn = indexOfLeftMostColumnWithOne
        return minColumn if minColumn != float('inf') else -1

    def getIndexOfLeftMostColumnWithOne(self, binaryMatrix: BinaryMatrix, index_row: int,
                                        start_column: int, end_column: int) -> int:
        length = end_column - start_column + 1
        if length == 0:
            return -1
        elif length == 1:
            return 0 if binaryMatrix.get(index_row, start_column) == 1 else -1
        else:
            if binaryMatrix.get(index_row, end_column) == 0:
                return -1
            middle = (length // 2) if length % 2 == 1 else (length // 2 - 1)
            if binaryMatrix.get(index_row, start_column + middle) == 1:
                result = self.getIndexOfLeftMostColumnWithOne(binaryMatrix, index_row, start_column,
                                                              start_column + middle)
                return result
            else:
                result = self.getIndexOfLeftMostColumnWithOne(binaryMatrix, index_row, start_column + middle + 1,
                                                              end_column)
                return middle + result + 1 if result >= 0 else -1

    def test(self, matrix: List[List[int]], result: int):
        binaryMatrix = BinaryMatrix(matrix)
        assert self.leftMostColumnWithOne(binaryMatrix) == result


